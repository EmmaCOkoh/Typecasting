# Typecasting = the process of converting a variable from one data type to another
#               str(), int(), float(), bool()

name = "Emmanuel Chuks Okoh"
age = 29
gpa = 5.6
is_student = False


print(type(name))
print(type(age))
print(type(gpa))
print(type(is_student))

# int to float
age = float(age)
print(age)
print(type(age))

# int to string
age = str(age)
print(age)
print(type(age))

# float to int
gpa = int(gpa)
print(gpa)
print(type(gpa))


